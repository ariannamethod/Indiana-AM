@dataclass
class ResonanceResult:
    """Outcome of a ``genesis2_resonance_loop`` invocation."""

    final_resonance: str
    layers: int
    evolution: float


def genesis2_resonance_loop(
    model: "Transformer",
    tokenizer: "ByteTokenizer",
    initial_prompt: str,
    generate_fn: Callable,
    iterations: int = 3,
    temperature: float = 0.8,
    max_new_tokens: int = 150,
    resonance_threshold: float = 0.7,
    device: torch.device = torch.device("cuda" if torch.cuda.is_available() else "cpu"),
) -> ResonanceResult:
    """Generate resonant responses by recursively feeding model outputs back as
    mutated prompts.
    """
    messages = [{"role": "user", "content": initial_prompt}]
    resonances: List[str] = []
    prev_output = None
    sim = 0.0
    for layer in range(iterations):
        prompt_tokens = tokenizer.apply_chat_template(messages, add_generation_prompt=True)
        completion_tokens = generate_fn(
            model, [prompt_tokens], max_new_tokens, tokenizer.eos_token_id, temperature
        )[0]
        output = tokenizer.decode(completion_tokens, skip_special_tokens=True)
        resonances.append(output)
        if prev_output:
            with torch.no_grad():
                vec_prev = model.embed_tokens(
                    torch.tensor(tokenizer.encode(prev_output), device=device)
                ).mean(dim=0)
                vec_curr = model.embed_tokens(
                    torch.tensor(tokenizer.encode(output), device=device)
                ).mean(dim=0)
                sim = torch.cosine_similarity(vec_prev, vec_curr, dim=0).item()
            if sim > resonance_threshold:
                break
        mutate_prompt = (
            f"{initial_prompt}\nPrevious echo: {output}\nResonate deeper: Rethink with paradox/glitch twist."
        )
        messages = [{"role": "user", "content": mutate_prompt}]
        prev_output = output
    final_echo = resonances[-1]
    return ResonanceResult(final_echo, len(resonances), sim)


def random_delay(min_seconds: int = 10, max_seconds: int = 40) -> None:
    """Sleep for a random period between ``min_seconds`` and ``max_seconds``."""
    delay = random.randint(min_seconds, max_seconds)
    time.sleep(delay)


def schedule_follow_up(
    history: List[Dict[str, str]],
    callback: Callable[[str], None],
    probability: float = 0.4,
    min_delay: int = 3600,
    max_delay: int = 10800,
) -> Future | None:
    """Optionally invoke ``callback`` with a follow-up message after a delay.

    Returns the ``Future`` representing the scheduled task or ``None`` if no task
    is scheduled.
    """

    if random.random() > probability:
        return None

    if not history:
        return None

    last = history[-1]["content"]
    delay = random.randint(min_delay, max_delay)

    def worker():
        time.sleep(delay)
        follow_up = (
            f"I thought again about our discussion: '{last}'. Here is an additional thought."
        )
        callback(follow_up)

    task = _executor.submit(worker)
    _follow_up_tasks.add(task)
    return task


def cancel_follow_up(task: Future) -> None:
    """Cancel a scheduled follow-up task and remove it from the registry."""

    task.cancel()
    _follow_up_tasks.discard(task)


def shutdown_executor() -> None:
    """Cleanly shut down the shared ``ThreadPoolExecutor``."""

    _executor.shutdown(wait=True)

