def act_quant(x: torch.Tensor, block_size: int = 128) -> Tuple[torch.Tensor, torch.Tensor]:
    """Simple stand-in for FP8 activation quantization."""

    return x.to(torch.float32), torch.ones_like(x[..., :1], dtype=torch.float32)


def weight_dequant(x: torch.Tensor, s: torch.Tensor, block_size: int = 128) -> torch.Tensor:
    """CPU fallback for weight dequantization."""

    return x.to(torch.get_default_dtype())


def fp8_gemm(a: torch.Tensor, a_s: torch.Tensor, b: torch.Tensor, b_s: torch.Tensor) -> torch.Tensor:
    """Simplified GEMM ignoring scaling factors (sufficient for tests)."""

    return a.to(torch.float32) @ b.to(torch.float32)


